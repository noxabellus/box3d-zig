var stdout_buf: [4096]u8 = undefined;
var stdout_writer: std.Io.File.Writer = undefined;
var stdout: *std.Io.Writer = undefined;

pub fn main(init: std.process.Init) anyerror!void {
    stdout_writer = std.Io.File.stdout().writer(init.io, &stdout_buf);
    stdout = &stdout_writer.interface;
    defer stdout_writer.flush() catch |err| {
        @branchHint(.cold);
        std.log.err("Failed to perform final flush of stdout: {s}", .{@errorName(err)});
    };

    try snapshot(stdout);
}

test {
    var output = std.Io.Writer.Allocating.init(std.testing.allocator);
    defer output.deinit();

    try snapshot(&output.writer);

    try std.testing.expectEqualStrings(expect, output.written());
}

fn snapshot(out: *std.Io.Writer) std.Io.Writer.Error!void {
    const timeStep = 1.0 / 60.0;
    const subStepCount = 4;

    var worldDef = b3.defaultWorldDef();
    worldDef.workerCount = 4;
    worldDef.gravity = b3.Vec3{ .x = 0.0, .y = -10.0, .z = 0.0 };

    const worldId = b3.createWorld(&worldDef);

    var groundBodyDef = b3.defaultBodyDef();
    groundBodyDef.position = b3.Vec3{ .x = 0.0, .y = -10.0, .z = 0.0 };

    const groundId = b3.createBody(worldId, &groundBodyDef);

    const groundBox = b3.makeBoxHull(50.0, 10.0, 50.0);

    const groundShapeDef = b3.defaultShapeDef();
    const groundShapeId = b3.createHullShape(groundId, &groundShapeDef, &groundBox.base);

    var bodyDef = b3.defaultBodyDef();
    bodyDef.type = .dynamic;
    bodyDef.position = b3.Vec3{ .x = 0.0, .y = 4.0, .z = 0.0 };
    const bodyId = b3.createBody(worldId, &bodyDef);

    const dynamicBox = b3.makeCubeHull(1.0);

    var shapeDef = b3.defaultShapeDef();
    shapeDef.density = 1.0;
    shapeDef.baseMaterial.friction = 0.3;

    const hullShapeId = b3.createHullShape(bodyId, &shapeDef, &dynamicBox.base);

    for (0..90) |i| {
        b3.World_step(worldId, timeStep, subStepCount);

        const position = b3.Body_getPosition(bodyId);
        const rotation = b3.Body_getRotation(bodyId);

        try out.print(
            "{d}: {d:4.2} {d:4.2} {d:4.2} {d:4.2} {d:4.2} {d:4.2} {d:4.2}\n",
            .{ i, position.x, position.y, position.z, rotation.v.x, rotation.v.y, rotation.v.z, rotation.s },
        );
    }

    _ = .{ groundShapeId, hullShapeId };
}

